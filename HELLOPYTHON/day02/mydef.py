def add(a,b):
    return a+b

def minus(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

sum = add(2, 3)
print("sum : ",sum)

minus = minus(2, 3)
print("minus : ",minus)
print("{} - {} = {}\n".format(2, 3, minus))

mul = multiply(2, 3)
print("multiply : ",mul)

# numeric -> 정수 실수의 경계를 허문다
div = divide(2, 3)
print("divide : ",div)


